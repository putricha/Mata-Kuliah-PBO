

public  class Mahasiswa {
        private String nama;
        private String noUjian;

        public Mahasiswa(String nama, String noUjian) {
            this.nama = nama;
            this.noUjian = noUjian;
        }

        public String getNama() {
            return nama;
        }

        public String getNoUjian() {
            return noUjian;
        }
    }


